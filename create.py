import cv2
import numpy as np

# === ШАГ 1: СОЗДАЁМ ПУСТОЕ ИЗОБРАЖЕНИЕ ===

# Создаём матрицу 300x300 пикселей, 3 канала (цветное), все значения = 0 (чёрный)
photo = np.zeros((300, 300, 3), dtype='uint8')

# === ШАГ 2: РИСУЕМ ПРЯМОУГОЛЬНИК ===

# Рисуем прямоугольник: от точки (10, 10) до (150, 200), цвет в BGR: (119, 201, 105)
cv2.rectangle(photo, (10, 10), (150, 200), (119, 201, 105), thickness=cv2.FILLED)
# Можно использовать thickness=3 для обводки вместо заливки

# === ШАГ 3: ЛИНИЯ СВЕРХУ ===

# Линия от (0, 0) до (100, 0), толщина 3
cv2.line(photo, (0, 0), (100, 0), (119, 201, 105), thickness=3)

# === ШАГ 4: РИСУЕМ КРУГ ===

# !!! ИСПРАВЛЕНО !!! — координаты центра должны быть кортежем
# Центр круга — середина изображения по X и Y
cv2.circle(photo, (photo.shape[1] // 2, photo.shape[0] // 2), 50, (119, 201, 105), thickness=cv2.FILLED)

# === ШАГ 5: ПИШЕМ ТЕКСТ ===

# Рисуем текст 'Sosy' в точке (100, 150), размер шрифта 1, цвет синий
cv2.putText(photo, 'Sosy', (100, 150), cv2.FONT_HERSHEY_TRIPLEX, 1, (255, 0, 0), thickness=1)

# === ШАГ 6: ПОКАЗЫВАЕМ ИЗОБРАЖЕНИЕ ===

cv2.imshow('Photo', photo)
cv2.waitKey(0)
cv2.destroyAllWindows()
